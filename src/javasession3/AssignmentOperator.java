package javasession3;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a=10;
	       
	     a +=5; 
	       
		System.out.println(a);// a=15
		
		a -= 10;
		System.out.println(a);//a=5
		
		a *= 15;
		System.out.println(a);//a=75
		
		a /=3;
		System.out.println(a);//a=25
	}

}
