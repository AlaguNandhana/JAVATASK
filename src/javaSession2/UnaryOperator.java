package javaSession2;

public class UnaryOperator {

	public static void main(String[] args) {
		//a++ : post increment
		//++a : pre increment
		//--a : pre decrement
		int a= 10;// local variable
		
		System.out.println(a++); //10
		System.out.println(a);
		System.out.println(a);
		System.out.println(a);
	}
	

}
